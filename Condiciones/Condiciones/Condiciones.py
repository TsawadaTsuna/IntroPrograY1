print("Programa que verifica si un numero esta entre un determinado intervalo")
#Entradas
l1 = int(input("Ingrese el limite inferior del intervalo:"))
l2 = int(input("Ingrese el limite superior del intervalo:"))
n = int(input("Ingrese el limite inferior del intervalo:"))
#Proceso y salida
if n>l1 and n <l2:
    print("Numero valido")
else:
    print("Numero invalido")